// Transform the data to match our Firestore structure
export const transformLocationData = () => {
  const states: any[] = [];
  const districts: any[] = [];
  let stateId = 1;
  let districtId = 1;

  Object.entries(locationData).forEach(([stateKey, stateData]) => {
    // Add state
    states.push({
      id: stateId,
      name: stateData.name,
      localName: stateData.localName,
      available: stateData.available,
    });

    // Add districts for this state
    Object.entries(stateData.districts).forEach(([districtKey, districtData]) => {
      districts.push({
        id: districtId,
        name: districtData.name,
        localName: districtData.localName,
        available: districtData.available,
        stateId: stateId,
      });
      districtId++;
    });

    stateId++;
  });

  return { states, districts };
};